import * as tf from "@tensorflow/tfjs";

export const predictWaterUsage = async (usageData) => {
  const xs = tf.tensor1d(usageData.map((_, i) => i));
  const ys = tf.tensor1d(usageData.map(u => u.litersUsed));

  const model = tf.sequential();
  model.add(tf.layers.dense({ units: 1, inputShape: [1] }));
  model.compile({ loss: "meanSquaredError", optimizer: "sgd" });

  await model.fit(xs, ys, { epochs: 100 });
  const nextIndex = usageData.length;
  const prediction = model.predict(tf.tensor1d([nextIndex]));
  return prediction.dataSync()[0];
};
